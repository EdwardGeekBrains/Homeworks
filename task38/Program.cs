/* Задача 38: Задайте массив вещественных чисел. Найдите разницу между максимальным и минимальным элементов массива.
[3.22, 4.2, 1.15, 77.15, 65.2] => 77.15 - 1.15 = 76
  */

using System;

class Program
{
    static void Main()
    {
        // Создаем массив вещественных чисел
        double[] array = { 3.22, 4.2, 1.15, 77.15, 65.2 };

        // Инициализируем переменные для хранения максимального и минимального элементов
        double min = array[0];
        double max = array[0];

        // Находим минимальный и максимальный элементы массива
        for (int i = 1; i < array.Length; i++)
        {
            if (array[i] < min)
            {
                min = array[i];
            }
            if (array[i] > max)
            {
                max = array[i];
            }
        }

        // Находим разницу между максимальным и минимальным элементами
        double difference = max - min;

        // Выводим результат на экран
        Console.WriteLine("Разница между максимальным и минимальным элементами массива: " + difference);
    }
}
